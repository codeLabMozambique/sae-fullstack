-- iteracao do usuario na aplicacao 
CREATE TABLE smartSAE.APP_TRANSACTION (
	ID NUMBER(38) NOT NULL,
   	STATUS NUMBER(1) NOT NULL,
    CODE VARCHAR2(64) NOT NULL,
    TYPE VARCHAR2(64) NOT NULL,
	LABEL VARCHAR2(256) NOT NULL,
	ROUTER_LINK VARCHAR2(256),
	POSITION NUMBER(38) NOT NULL,
	PARENT_ID NUMBER(38),

	CONSTRAINT PK_APP_TRANSACTION PRIMARY KEY (ID),
  	CONSTRAINT TC_APP_TRANSACTION_STATUS CHECK (STATUS IN (0,1)),
  	CONSTRAINT FK_APP_TRANSACTION_01 FOREIGN KEY (PARENT_ID) REFERENCES smartSAE.APP_TRANSACTION(ID)
);

CREATE SEQUENCE smartSAE.APP_TRANSACTION_SEQUENCE START WITH 100 INCREMENT BY 1 NOCACHE;
