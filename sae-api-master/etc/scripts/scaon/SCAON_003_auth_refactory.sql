-- role da transacao do usuario
CREATE TABLE smartSAE.ROLE_TRANSACTION (
	ID NUMBER(38) NOT NULL,
   	STATUS NUMBER(1) NOT NULL,
    ROLE VARCHAR2(64) NOT NULL,
	APP_TRANSACTION_ID NUMBER(38),
	
	CONSTRAINT PK_ROLE_TRANSACTION PRIMARY KEY (ID),
  	CONSTRAINT TC_ROLE_TRANSACTION_STATUS CHECK (STATUS IN (0,1)),
  	CONSTRAINT FK_ROLE_TRANSACTION_01 FOREIGN KEY (APP_TRANSACTION_ID) REFERENCES smartSAE.APP_TRANSACTION(ID)
);

